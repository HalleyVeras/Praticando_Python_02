#Pergunta 1: Qual é a saída do trecho de código?


lst = ["D", "F", "A", "Z"]
lst.sort()

print(lst)
#Resposta ['A', 'D', 'F', 'Z']

#Pergunta 2: Qual é a saída do trecho de código?
a = 3
